public class WhileLoopEvenOddNumber {
    public static void main(String[] args) {
        //Even number
        int i =2;
        while(i < 12) {
            System.out.println(i * 2);
            i++;

        }
                //print odd number
        int y=1;
        while(y <19){
            System.out.println(+y);
            y=y+2;

        }
    }
}
