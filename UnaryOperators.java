public class UnaryOperators {
    public static void main(String[] args){

        int a =21;
        boolean b = true;
        System.out.println(a++); //returns 21
        System.out.println(++a); //returns 23
        System.out.println(a--); //returns 23
        System.out.println(--a); //returns 21
        System.out.println(!(b)); //returns 21



    }
}
